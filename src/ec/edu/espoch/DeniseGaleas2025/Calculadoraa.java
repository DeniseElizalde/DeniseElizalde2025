public class Calculadoraa {
    private int suma;
    private int deshacer;
    private int rehacer;
    private int resultado;
    private int salir;

    public Calculadoraa(int suma, int deshacer, int rehacer, int resultado, int salir) {
        this.suma = suma;
        this.deshacer = deshacer;
        this.rehacer = rehacer;
        this.resultado = resultado;
        this.salir = salir;
    }

    public int getSuma() {
        return suma;
    }

    public void setSuma(int suma) {
        this.suma = suma;
    }

    public int getDeshacer() {
        return deshacer;
    }

    public void setDeshacer(int deshacer) {
        this.deshacer = deshacer;
    }

    public int getRehacer() {
        return rehacer;
    }

    public void setRehacer(int rehacer) {
        this.rehacer = rehacer;
    }

    public int getResultado() {
        return resultado;
    }

    public void setResultado(int resultado) {
        this.resultado = resultado;
    }

    public int getSalir() {
        return salir;
    }

    public void setSalir(int salir) {
        this.salir = salir;
    }
    

    
     
}
